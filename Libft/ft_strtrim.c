#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set);

static	int	in_set(char c, char const *set)
{
	size_t	i;

	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;

	start = 0;
	end = ft_strlen(s1);
	if (!s1)
		return (ft_strdup(""));
	if (!set)
		return (ft_strdup(s1));
	while (in_set(s1[start], set))
		start++;
	while (in_set(s1[end - 1], set))
		end--;
	return (ft_substr(s1, start, (end - start)));
}
